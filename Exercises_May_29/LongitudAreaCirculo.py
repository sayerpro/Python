# CALCULO LONGITUD CIRCUNFERENCIA Y ÁREA DE UN CÍRCULO
# ENTRADAS
print("============================================")
radio = float(input("Digite el radio del Círculo: "))
print("============================================")

# PROCESO
area = 3.1416 * radio ** 2
length = radio * 2 * 3.1416

# SALIDA
print("==============================================================")
print("RESULTADO CALCULO LONGITUD CIRCUNFERENCIA Y ÁREA DE UN CÍRCULO")
print("Longitud de la Circunferencia: ", length)
print("Área de la Circunferencia: ", area)
print("==============================================================")
