# CALCULO PROMEDIO
# ENTRADAS
print("===================================================")
numberOne = float(input("Digite el primer número: "))
numberTwo = float(input("Digite el segundo número: "))
numberThree = float(input("Digite el tercer número: "))
numberFour = float(input("Digite el cuarto número: "))
numberFive = float(input("Digite el quinto número: "))

# PROCESOS
promedio = (numberOne + numberTwo + numberThree + numberFour + numberFive) / 5

# SALIDAS
print("El promedio de los números ingresados es: ", promedio)
