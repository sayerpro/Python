print("Resuelva la siguiente ecuaci√≥n: ((x + y) / 2) * ((a-b) / 5.0)")

# PROCESOS
x = 10
y = 5
a = 3
b = 7

resultado = ((x + y) / 2) * ((a-b) / 5.0)

operacionUno = x + y
