# CALCULO AREA DE UN TRIANGULO
# ENTRADAS
print("============================================")
base = int(input("Digite el valor de la base: "))
height = int(input("Digite el valor de la altura: "))
print("============================================")

# PROCESOS
surface = base * height / 2

# SALIDAS
print("============================================")
print("RESULTADO CALCULO DEL ÁREA DE UN TRIÁNGULO")
print("Base del Triángulo: ", base)
print("Altura del Triángulo: ", height)
print("El área calculada del Triángulo es: ", surface)
print("============================================")
