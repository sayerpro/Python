# OPERACIONES MATEMATICAS
# ENTRADAS
print("============================================")
numberOne = int(input("Digite el primer número: "))
numberTwo = int(input("Digite el segundo número: "))
print("============================================")
numberThree = 10

# PROCESOS
sum = numberOne + numberTwo + numberThree
rest = numberOne - numberTwo - numberThree
product = numberOne * numberTwo * numberThree
division = numberOne / numberTwo / numberThree
wholeDivision = numberOne // numberTwo // numberThree
power = numberOne ** numberTwo
residue = numberOne % numberTwo % numberThree
denial = -numberOne + -numberTwo - -numberThree

# SALIDAS
print("====================================================================")
print("RESULTADO OPERACIONES MATEMÁTICAS")
print("Las operaciones matematicas se hicieron con los siguientes números:")
print("Número 1 = ", numberOne)
print("Número 2 = ", numberTwo)
print("Número 3 = ", numberThree)
print("El resultado de la SUMA es: ", sum)
print("El resultado de la RESTA es: ", rest)
print("El resultado del PRODUCTO es: ", product)
print("El resultado de la DIVISIÓN DECIMAL es: ", division)
print("El resultado de la DIVISIÓN ENTRA es: ", wholeDivision)
print("El resultado de la POTENCIA es: ", power)
print("El resultado del RESIDUO es: ", residue)
print("El resultado de la NEGACIÓN es: ", denial)
print("====================================================================")
